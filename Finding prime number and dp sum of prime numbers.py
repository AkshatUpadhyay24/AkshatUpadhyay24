# Finding prime number and dp sum of prime numbers

n1=int(input("start number "  ))

n2=int(input("end number " ))

sum=0

a=range(n1,n2)

for i in a:
    
    if (i%3==0):
        print(" ")

    elif (i%2==0): 
        print(" ")
    elif (i%5==0):
        print(" ")
    else:
        print(i,"=","prime number ")
        
        sum+=i
         
print(sum,"=", "total of prime number ")
        
 
 

      
      
      
      
      
      
      
      
      
    

       
        
              
        
        
        
        
        
    
        
    
        
        
             
        
        
    
     
    
        
        
         
            
        
        
    
        
   
            
        
        
        
 
 
            
        
        
        
        
    
        
    

       
     
    
   
    
         
    

                 